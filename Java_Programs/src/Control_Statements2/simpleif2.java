package Control_Statements2;
public class simpleif2 
{
	public static void main(String[] args) 
	{
	    int a = 10;
	    int b = 20;
	    int c = a+b;
	    
	    if(c>10)
	    {
	    	System.out.println("C is greater than 10");
	    }
	    
	    else
	    {
	    	System.out.println("C is less than 10");
	    }
	    
	    System.out.println("Program is completed");
	}
}
