package Arithmetic;

public class Relational 
{
	public static void main(String[] args) 
	{
		int a = 50;
		int b = 10;
		
	   System.out.println(a>b);
	   System.out.println(a<b);
	   System.out.println(a>=b);
	   System.out.println(a<=b);
	   System.out.println(a==b);
	   System.out.println(a!=b);
	}
	
}
