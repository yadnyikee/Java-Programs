package Arithmetic;

public class Increment 
{
	public static void main(String[] args) 
	{
		int a = 10;
		int b = 10;
		int c;
	
		
		c=a; // Assignment Operator
		System.out.println(c);
		
		a++; // Increment Operator
		System.out.println(a);
		
		c=c+1;
		System.out.println(c);
		
		c=c+3;
		System.out.println(c);
		System.out.println();
		
		// Decrement Operator
		c--;
		System.out.println(c);
		
		c=c-1;
		System.out.println(c);
		
		c=c-2;
		System.out.println(c);
	}
}
